package exemplo01;

public class Sobrecarga {
    public double somar(double n1, double n2){
        return (n1 + n2);
    }
    
    public double somar(double n1, double n2, double n3){
        return (n1 + n2 + n3);
    }
    
    public double somar(double n1, double n2, double n3, double n4){
        return (n1 + n2 + n3 + n4);
    }
}
